public class Concatination_Day_25 {

	public static void main(String[] args) {
		String firstname="rama";
		String lastnametname="gopireddy";
		
		//With out space
		System.out.println(firstname.concat(lastnametname));
		
//With space
		System.out.println(firstname.concat(" ").concat(lastnametname));
		
		
	}

}
